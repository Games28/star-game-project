	bool Collision(const Xwing& wing) const
	{
		const int wingright = wing.GetX() + wing.GetWidth();
		const int wingbottom = wing.GetY() + wing.GetHieght();
		const int Dright = x + dimension;
		const int Dbottom = y + dimension;

		return
			wingright >= x &&
			wingbottom >= y &&
			Dright >= wing.GetX() &&
			Dbottom >= wing.GetY();
	}
